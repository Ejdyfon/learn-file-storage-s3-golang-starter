func (cfg *apiConfig) handlerUploadVideo(w http.ResponseWriter, r *http.Request) {
	videoIDString := r.PathValue("videoID")
	videoID, err := uuid.Parse(videoIDString)
	if err != nil {
		respondWithError(w, http.StatusBadRequest, "Invalid ID", err)
		return
	}

	token, err := auth.GetBearerToken(r.Header)
	if err != nil {
		respondWithError(w, http.StatusUnauthorized, "Couldn't find JWT", err)
		return
	}

	userID, err := auth.ValidateJWT(token, cfg.jwtSecret)
	if err != nil {
		respondWithError(w, http.StatusUnauthorized, "Couldn't validate JWT", err)
		return
	}

	const maxMemory = 1 << 30 // 1 GB
	r.ParseMultipartForm(maxMemory)

	video, err := cfg.db.GetVideo(videoID)
	if err != nil {
		respondWithError(w, http.StatusInternalServerError, "Couldn't find video", err)
		return
	}
	if video.UserID != userID {
		respondWithError(w, http.StatusUnauthorized, "Not authorized to update this video", nil)
		return
	}

	file, header, err := r.FormFile("video")
	if err != nil {
		respondWithError(w, http.StatusBadRequest, "Unable to parse form file", err)
		return
	}
	defer file.Close()

	mediaType := header.Header.Get("Content-Type")
	if mediaType == "" {
		respondWithError(w, http.StatusBadRequest, "Missing Content-Type for thumbnail", nil)
		return
	}

	key := make([]byte, 32)
	rand.Read(key)

	salt := base64.RawURLEncoding.EncodeToString(key)
	newF, err := os.CreateTemp("", "tubely-upload.mp4")
	defer os.Remove("tubely-upload.mp4")
	defer newF.Close()

	if err != nil {
		respondWithError(w, http.StatusInternalServerError, "Cannot save video", err)
		return
	}
	io.Copy(newF, file)
	aspect, err := getVideoAspectRatio(newF.Name())
	salt = fmt.Sprintf("/%v/%v", aspect, salt)
	if err != nil {
		respondWithError(w, http.StatusInternalServerError, "Cannot parse video", err)
		return
	}
	newF.Seek(0, io.SeekStart)
	parms := s3.PutObjectInput{}
	parms.Bucket = &cfg.s3Bucket
	parms.Key = &salt
	parms.Body = newF
	parms.ContentType = &mediaType

	cfg.s3client.PutObject(context.TODO(), &parms)

	fileUrl := fmt.Sprintf("https://%v.s3.%v.amazonaws.com/%v", cfg.s3Bucket, cfg.s3Region, salt)
	video.VideoURL = &fileUrl
	err = cfg.db.UpdateVideo(video)
	if err != nil {
		respondWithError(w, http.StatusInternalServerError, "Couldn't update video", err)
		return
	}

	respondWithJSON(w, http.StatusOK, video)
}

func getVideoAspectRatio(filePath string) (string, error) {
	newBuffer := exec.Command("ffprobe", "-v", "error", "-print_format", "json", "-show_streams", filePath)
	resu, _ := newBuffer.Output()

	var result ResultOfFProbe
	if err := json.Unmarshal(resu, &result); err != nil {
		return "", fmt.Errorf("Error unmarshalling JSON: %v", err)
	}

	aspectVal := result.Streams[0].Width / result.Streams[0].Height
	aspect := "other"
	if aspectVal == 16/9 {
		aspect = "landscape"
	} else if aspectVal == 9/16 {
		aspect = "portrait"
	}
	return aspect, nil
}

